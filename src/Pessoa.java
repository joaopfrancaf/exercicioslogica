public class Pessoa {
    private char sexo;
    private double altura;

    public void setSexo (char sexo) {
        this.sexo = sexo;
    }

    public char getSexo () {
        return this.sexo;
    }

    public void setAltura (double altura) {
        this.altura = altura;
    }

    public double getAltura () {
        return this.altura;
    }
}
